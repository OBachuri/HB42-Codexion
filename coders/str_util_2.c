#include	"codexion.h"

char	*ft_itoa(int n)
{
	char	str_[31];
	int		i_;
	int		r_;
	int		s_;

	str_[30] = 0;
	i_ = 0;
	s_ = (n < 0);
	if (n > 0)
		n = -n;
	while (n || !i_)
	{
		r_ = n % 10;
		n = n / 10;
		i_++;
		str_[30 - i_] = '0' - r_;
	}
	if (s_)
		str_[30 - ++i_] = '-';
	return (ft_strdup(&str_[30 - i_]));
}
